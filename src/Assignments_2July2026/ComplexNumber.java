package Assignments_2July2026;

public class ComplexNumber {
// a+ib ; a-->real  b-->imaginary (eg=2+7i)
    double real;
    double imaginary;
    public ComplexNumber() {
        super();
    }
    public ComplexNumber(double real, double imaginary) {
        super();
        this.real = real;
        this.imaginary = imaginary;
    }


}
